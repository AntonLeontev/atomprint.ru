      <button data-toggle-id="faq_question_answer_1" class="faq_question_title">Вы заправляете картриджи на месте?</button>
      <div id="faq_question_answer_1" class="faq_question_answer">
        Картриджи нужно чистить с применением сильнопахнущей химии и очищать от остатков тонера. На месте сделать это качественно невозможно. Поэтому мы забираем картриджи в мастерскую. Готовы они будут на следующий день. Если у вас нет подменного картриджа мы привезем свой уже заправленный. Вы сможете работать на нем, пока он не закончится.
